namespace System.Windows.Forms;

/// <summary>Specifies the appearance of the border for a <see cref="T:System.Windows.Forms.StatusBarPanel" /> on a <see cref="T:System.Windows.Forms.StatusBar" /> control.</summary>
/// <filterpriority>2</filterpriority>
public enum StatusBarPanelBorderStyle
{
	/// <summary>No border is displayed.</summary>
	None = 1,
	/// <summary>The <see cref="T:System.Windows.Forms.StatusBarPanel" /> is displayed with a three-dimensional raised border.</summary>
	Raised,
	/// <summary>The <see cref="T:System.Windows.Forms.StatusBarPanel" /> is displayed with a three-dimensional sunken border.</summary>
	Sunken
}
