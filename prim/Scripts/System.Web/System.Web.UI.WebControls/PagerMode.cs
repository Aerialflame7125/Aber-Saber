namespace System.Web.UI.WebControls;

/// <summary>Represents the mode of the pager for accessing various pages within the <see cref="T:System.Web.UI.WebControls.DataGrid" /> control.</summary>
public enum PagerMode
{
	/// <summary>A pager with <see langword="Previous" /> and <see langword="Next" /> buttons to access the next and previous pages.</summary>
	NextPrev,
	/// <summary>A pager with numbered buttons to access pages directly.</summary>
	NumericPages
}
