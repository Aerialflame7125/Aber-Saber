namespace System.Web.UI.WebControls;

/// <summary>Specifies the different types of buttons that can be rendered in a Web Forms page.</summary>
public enum ButtonType
{
	/// <summary>A command button.</summary>
	Button,
	/// <summary>A button that displays an image.</summary>
	Image,
	/// <summary>A hyperlink-style button.</summary>
	Link
}
