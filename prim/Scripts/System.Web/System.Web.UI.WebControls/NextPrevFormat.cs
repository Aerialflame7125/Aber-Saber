namespace System.Web.UI.WebControls;

/// <summary>Represents the display format for the previous and next month navigation controls within the <see cref="T:System.Web.UI.WebControls.Calendar" />.</summary>
public enum NextPrevFormat
{
	/// <summary>Custom text format for the month navigation controls on the <see cref="T:System.Web.UI.WebControls.Calendar" />.</summary>
	CustomText,
	/// <summary>Abbreviated month name format for the month navigation controls on the <see cref="T:System.Web.UI.WebControls.Calendar" />. For example, "Jan".</summary>
	ShortMonth,
	/// <summary>Full month name format for the month navigation controls on the <see cref="T:System.Web.UI.WebControls.Calendar" />. For example, "January".</summary>
	FullMonth
}
