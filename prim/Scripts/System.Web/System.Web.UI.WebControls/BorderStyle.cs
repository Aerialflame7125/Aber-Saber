namespace System.Web.UI.WebControls;

/// <summary>Specifies the border style of a control.</summary>
public enum BorderStyle
{
	/// <summary>No set border style.</summary>
	NotSet,
	/// <summary>No border.</summary>
	None,
	/// <summary>A dotted line border.</summary>
	Dotted,
	/// <summary>A dashed line border.</summary>
	Dashed,
	/// <summary>A solid line border.</summary>
	Solid,
	/// <summary>A double solid line border.</summary>
	Double,
	/// <summary>A grooved border for a sunken border appearance.</summary>
	Groove,
	/// <summary>A ridged border for a raised border appearance.</summary>
	Ridge,
	/// <summary>An inset border for a sunken control appearance.</summary>
	Inset,
	/// <summary>An outset border for a raised control appearance.</summary>
	Outset
}
