namespace System.Web.UI.WebControls;

/// <summary>Specifies the position of the pager for accessing various pages within a paginated control.</summary>
public enum PagerPosition
{
	/// <summary>A pager positioned at the bottom of the control.</summary>
	Bottom,
	/// <summary>A pager positioned at the top of the control.</summary>
	Top,
	/// <summary>Pagers positioned at both the top and the bottom of the control.</summary>
	TopAndBottom
}
