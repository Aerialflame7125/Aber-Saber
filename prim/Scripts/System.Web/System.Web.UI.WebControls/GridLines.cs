namespace System.Web.UI.WebControls;

/// <summary>Specifies the gridline styles for controls that display items in a table structure.</summary>
public enum GridLines
{
	/// <summary>No gridlines are rendered.</summary>
	None,
	/// <summary>Only horizontal gridlines are rendered.</summary>
	Horizontal,
	/// <summary>Only vertical gridlines are rendered.</summary>
	Vertical,
	/// <summary>Both horizontal and vertical gridlines are rendered.</summary>
	Both
}
