namespace System.Web.UI.WebControls;

/// <summary>Specifies the general layout of items within a composite control. </summary>
public enum Orientation
{
	/// <summary>Lays out the elements of the control in a horizontal manner. Most controls are side-by-side, with very little stacking of controls.</summary>
	Horizontal,
	/// <summary>Lays out the elements of the control in a vertical manner. Most controls are stacked, with few controls side-by-side.</summary>
	Vertical
}
