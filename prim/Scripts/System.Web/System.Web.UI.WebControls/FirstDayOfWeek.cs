namespace System.Web.UI.WebControls;

/// <summary>Specifies the day to display as the first day of the week on the <see cref="T:System.Web.UI.WebControls.Calendar" /> control.</summary>
public enum FirstDayOfWeek
{
	/// <summary>Indicates that the first day of the week is Sunday.</summary>
	Sunday,
	/// <summary>Indicates that the first day of the week is Monday.</summary>
	Monday,
	/// <summary>Indicates that the first day of the week is Tuesday.</summary>
	Tuesday,
	/// <summary>Indicates that the first day of the week is Wednesday.</summary>
	Wednesday,
	/// <summary>Indicates that the first day of the week is Thursday.</summary>
	Thursday,
	/// <summary>Indicates that the first day of the week is Friday.</summary>
	Friday,
	/// <summary>Indicates that the first day of the week is Saturday.</summary>
	Saturday,
	/// <summary>Indicates that the first day of the week is specified by the system settings.</summary>
	Default
}
