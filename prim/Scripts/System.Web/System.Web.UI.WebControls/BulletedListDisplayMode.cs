namespace System.Web.UI.WebControls;

/// <summary>Specifies the display behaviors that you can apply to the list item contents in a <see cref="T:System.Web.UI.WebControls.BulletedList" /> control.</summary>
public enum BulletedListDisplayMode
{
	/// <summary>Displays the list item content as text.</summary>
	Text,
	/// <summary>Displays the list item content as hyperlinks.</summary>
	HyperLink,
	/// <summary>Displays the list item content as link buttons.</summary>
	LinkButton
}
